# Listas: são coleções ordenadas e mutáveis. Permitem elementos duplicados.
# Exemplo:
lista = [1, 2, 3, 4, 5]
print(lista)  # Saída: [1, 2, 3, 4, 5]

# Tuplas: são coleções ordenadas e imutáveis. Permitem elementos duplicados.
# Exemplo:
tupla = (1, 2, 3, 4, 5)
print(tupla)  # Saída: (1, 2, 3, 4, 5)

# Dicionários: são coleções não ordenadas de pares chave-valor. Não permitem chaves duplicadas.
# Exemplo:
dicionario = {"nome": "Gabriel", "idade": 25}
print(dicionario)  # Saída: {'nome': 'Gabriel', 'idade': 25}

# Mapas: em Python, geralmente referem-se a objetos do tipo `dict`, que são usados para mapeamento de chave-valor.
# Exemplo:
mapa = {"a": 1, "b": 2, "c": 3}
print(mapa)  # Saída: {'a': 1, 'b': 2, 'c': 3}

# Conjuntos: são coleções não ordenadas e não permitem elementos duplicados.
# Exemplo:
conjunto = {1, 2, 3, 4, 5}
print(conjunto)  # Saída: {1, 2, 3, 4, 5}

